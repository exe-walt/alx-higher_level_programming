#!/usr/bin/python3

"""Task 6 - A program that prints all possible different combinations of two digits."""

for num1 in range(0, 10):
    for num2 in range(num1 + 1, 10):
        if num1 == 8 and num2 == 9:
            print("{}{}".format(num1, num2))
        else:
            print("{}{}".format(num1, num2), end=", ")
